public class bitwiseor
{
    public static void main(String[] args)
    {
        System.out.println(11|18);
        System.out.println(22|144);
        System.out.println(88|122);
        System.out.println(11&27);
        System.out.println(88&122);
        System.out.println(18&12);
    }
}